const generatedKnowledgeFiles = Array.from({ length: 1352 }, (_, index) => {
  const seed = knowledgeFileSeeds[index % knowledgeFileSeeds.length]
  const batch = Math.floor(index / knowledgeFileSeeds.length) + 1
  const baseDay = ((index % 20) + 1).toString().padStart(2, '0')
  const titleSuffix =
    seed.categoryId === 'meeting'
      ? `（第${batch}批归档）`
      : seed.categoryId === 'solution'
        ? `（专题批次${batch}）`
        : `（修订批次${batch}）`

  return {
    ...seed,
    id: `${seed.id}-g${batch}`,
    title: `${seed.title}${titleSuffix}`,
    version:
      batch % 3 === 0
        ? seed.version
        : `V${Math.max(1, Number(seed.version.replace(/[^\d.]/g, '') || '1')).toFixed(1)}`,
    updatedAt: `2025-04-${baseDay} ${String(9 + (index % 9)).padStart(2, '0')}:${index % 2 === 0 ? '20' : '45'}`,
    fileCode: `${seed.fileCode}-${String(batch).padStart(3, '0')}`,
    hasNewVersion: index % 11 === 0 ? true : seed.hasNewVersion,
    needsComment: index % 17 === 0 ? true : seed.needsComment,
    needsReview: index % 23 === 0 ? true : seed.needsReview,
    updatedThisMonth: index % 5 === 0 ? false : seed.updatedThisMonth,
  }
})

